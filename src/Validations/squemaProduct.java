    public boolean validation_product(product dataProduct) {
        //Name
        if (dataProduct.getCodeProduct().equals("")) {
            alerts.alert("El codigo es obligatorio");
            return false;
        }
        if (dataProduct.getCodeProduct().length() > 25) {
            alerts.alert("El codigo es m치ximo de 25 caracteres");
            return false;
        }
        if (dataProduct.getCodeProduct().length() < 1) {
            alerts.alert("El codigo es minimo de 1 caracteres");
            return false;
        }

        //Description
        if (dataProduct.getDescription().length() > 75) {
            alerts.alert("La descripcion es m치ximo de 75 caracteres");
            return false;
        }
        if (!dataProduct.getDescription().equals("") & dataProduct.getDescription().length() < 5) {
            alerts.alert("La descripcion es minimo de 5 caracteres");
            return false;
        }

        //Price
        if (dataProduct.getPrice() == null) {
            alerts.alert("El precio es obligatorio");
            return false;
        }
        if (dataProduct.getPrice() <= 0) {
            alerts.alert("El precio tiene que ser mayor a 0");
            return false;
        }

        //Cant
        if (dataProduct.getCant() == null) {
            alerts.alert("La cantidad es obligatoria");
            return false;
        }
        if (dataProduct.getCant() <= 0) {
            alerts.alert("El cantidad tiene que ser mayor a 0");
            return false;
        }

        //Description1
        if (dataProduct.getDescription1().length() > 75) {
            alerts.alert("La descripcion1 es m치ximo de 75 caracteres");
            return false;
        }
        if (!dataProduct.getDescription1().equals("") & dataProduct.getDescription1().length() < 5) {
            alerts.alert("La descripcion1 es minimo de 5 caracteres");
            return false;
        }

        //Description2
        if (dataProduct.getDescription2().length() > 75) {
            alerts.alert("La descripcion1 es m치ximo de 75 caracteres");
            return false;
        }
        if (!dataProduct.getDescription2().equals("") & dataProduct.getDescription2().length() < 5) {
            alerts.alert("La descripcion1 es minimo de 5 caracteres");
            return false;
        }

        return true;
    }


