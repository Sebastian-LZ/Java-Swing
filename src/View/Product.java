    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        product dataProduct = new product();
        controller.deleteProduct();

        for (int i = 0; i < tblProducts.getRowCount(); i++) {
            dataProduct.setCodeProduct(tblProducts.getValueAt(i, 0).toString());
            dataProduct.setDescription(tblProducts.getValueAt(i, 1).toString());
            dataProduct.setPrice(Double.parseDouble(tblProducts.getValueAt(i, 2).toString()));
            dataProduct.setCant(Double.parseDouble(tblProducts.getValueAt(i, 3).toString()));
            dataProduct.setDescription1(tblProducts.getValueAt(i, 4).toString());
            dataProduct.setDescription2(tblProducts.getValueAt(i, 5).toString());
            if (squema.validation_product(dataProduct)) {
                if (controller.createProduct(dataProduct)) {
                    modelTable.removeRow(i);
                    i = i - 1;
                }
            }
        }

        alerts.alertSuccess("Productos registrados con exito");
        btnSave.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed

