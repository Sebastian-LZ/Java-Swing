    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser examinar = new JFileChooser();
        examinar.setFileFilter(new FileNameExtensionFilter("Archivos excel", "xls", "xlsx"));
        int opcion = examinar.showOpenDialog(this);
        File archivoExcel = null;

        if (opcion == JFileChooser.APPROVE_OPTION) {
            archivoExcel = examinar.getSelectedFile().getAbsoluteFile();

            while (tblProducts.getRowCount() > 0) {
                modelTable.removeRow(0);
            }

            try {
                Workbook leerExcel = Workbook.getWorkbook(archivoExcel);
                for (int hoja = 0; hoja < leerExcel.getNumberOfSheets(); hoja++) {
                    Sheet hojaP = leerExcel.getSheet(hoja);
                    int count = 0;
                    Object[] listBadProducts = new Object[hojaP.getRows()];
                    for (int fila = 0; fila < hojaP.getRows(); fila++) {
                        if (fila > 0) {
                            if (!hojaP.getCell(0, fila).getContents().equals("")) {
                                String description1 = "", description2 = "";
                                try {
                                    description1 = hojaP.getCell(4, fila).getContents();
                                } catch (Exception e) {
                                }

                                try {
                                    description2 = hojaP.getCell(5, fila).getContents();
                                } catch (Exception e) {
                                }

                                try {
                                    modelTable.addRow(
                                            new Object[]{hojaP.getCell(0, fila).getContents(), hojaP.getCell(1, fila).getContents(),
                                                Double.parseDouble(hojaP.getCell(2, fila).getContents().replace(",", "")),
                                                Double.parseDouble(hojaP.getCell(3, fila).getContents().replace(",", "")),
                                                description1, description2});
                                } catch (Exception e) {
                                    listBadProducts[count] = hojaP.getCell(0, fila).getContents();
                                    count++;
                                }
                            }
                        }
                    }

                    if (count > 0) {
                        if (!listBadProducts[0].equals("")) {
                            String message = "";
                            for (Object listBadProduct : listBadProducts) {
                                if (listBadProduct != null) {
                                    message = message + listBadProduct + ", ";
                                }
                            }
                            alerts.bigAlert("La cantidad y el precio debén de ser números [" + message.substring(0, message.length() - 2) + "]");
                        }
                    }

                    if (tblProducts.getRowCount() > 0) {
                        btnSave.setEnabled(true);
                    }

                }
            } catch (IOException | BiffException ex) {
                alerts.alert("El archivo de excel es de una versión no compatible");
                return;
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

