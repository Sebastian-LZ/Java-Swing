    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        if (tableData.getSelectedRow() > -1) {
            events();
        } else {
            alerts.alert("Debe seleccionar un registro");
        }
    }//GEN-LAST:event_btnSelectActionPerformed

    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        if (evt.getClickCount() > 1) {
            events();
        }
    }//GEN-LAST:event_tableDataMouseClicked

    private void txtIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdKeyReleased
        modelFilter.setRowFilter(RowFilter.regexFilter("(?i)" + txtId.getText(), 0));
        txtDescription.setText("");
    }//GEN-LAST:event_txtIdKeyReleased

    private void txtDescriptionKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDescriptionKeyReleased
        modelFilter.setRowFilter(RowFilter.regexFilter("(?i)" + txtDescription.getText(), 1));
        txtId.setText("");
    }//GEN-LAST:event_txtDescriptionKeyReleased

    /**
     * @param args the command line arguments
     */
    public void events() {
        switch (from) {
            case "viewUser":
                this.dispose();
                viewUser.uploadData(tableData.getValueAt(tableData.getSelectedRow(), 1).toString());
                break;
            case "viewWallpaper":
                this.dispose();
                viewWallpaper.uploadData(tableData.getValueAt(tableData.getSelectedRow(), 0).toString());
                break;
            case "viewCover":
                this.dispose();
                viewCover.uploadData(tableData.getValueAt(tableData.getSelectedRow(), 0).toString());
                break;
        }
    }

