    public Seeker(java.awt.Frame parent, boolean modal, String table, String dataToSelect, String condition, String orderSql,
            String tableNames[], String dataBaseName[], String from) {
        super(parent, modal);
        initComponents();

        this.from = from;
        this.setLocationRelativeTo(null);
        DefaultTableModel modelTable = controller.openSeeker(table, dataToSelect, condition, orderSql, tableNames, dataBaseName);
        tableData.setModel(modelTable);
        modelFilter = new TableRowSorter<>(modelTable);
        tableData.setRowSorter(modelFilter);

        this.getRootPane().registerKeyboardAction(accion("close", this), KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
    }

    private ActionListener accion(final String opc, final JDialog ventana) {
        ActionListener a = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                switch (opc) {
                    case "close":
                        ventana.dispose();
                        break;
                }
            }
        };
        return a;
    }

